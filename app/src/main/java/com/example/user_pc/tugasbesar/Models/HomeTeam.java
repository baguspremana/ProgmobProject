package com.example.user_pc.tugasbesar.Models;

public class HomeTeam {

    int logo;
    String name;

    public HomeTeam(int logo, String name) {
        this.logo = logo;
        this.name = name;
    }

    public int getLogo() {
        return logo;
    }

    public String getName() {
        return name;
    }
}
